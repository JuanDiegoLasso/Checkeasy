    private boolean ExisteUsuario (){
        boolean valida = false;
        for (int i = 0; i < Tablausuarios.getRowCount(); i++) {
            if (Tablausuarios.getValueAt(i, 0).equals(txtusuario.getText()) ){
                valida = true;
                break;
            }    
        }
        return valida;
    }   
    private void FillTableUsers(){
        Servicio objusuarios = new Servicio();
        List<UserEmploy> usuarios = objusuarios.ConsultarUsuarios();
        var model = (DefaultTableModel) Tablausuarios.getModel();
        limpiarTabla();
        for(UserEmploy obj: usuarios){
            model.addRow(new Object[]{
                obj.getUsuario(), obj.getNombre(), obj.getCorreo(),obj.getTelefono()
            });
        }

    }
    private void limpiarTabla(){
         DefaultTableModel model = (DefaultTableModel) Tablausuarios.getModel();    
        while (Tablausuarios.getRowCount() >0){
            model.removeRow(0);
        }
    }    
            
