    private void bntInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntInsertarActionPerformed
        // TODO add your handling code here:
        if (txtnombre.getText().isEmpty() || txtusuario.getText().isEmpty() || txtcorreo.getText().isEmpty() || txttelefono.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingrese todos los campos. Intente de nuevo","Informacion", JOptionPane.INFORMATION_MESSAGE);            
        }            
        else {
            if (!ExisteUsuario()){            
                int ninteger = Integer.parseInt(txttelefono.getText());
                UserEmploy p5 = new UserEmploy (txtusuario.getText(),"123",txtnombre.getText(), txtcorreo.getText(), ninteger);
                try {
                    p5.CrearUsuario();
                } catch (SQLException ex) {
                    Logger.getLogger(PanelUsers.class.getName()).log(Level.SEVERE, null, ex);
                }
                //adiciona a la tabla
                DefaultTableModel model = (DefaultTableModel) Tablausuarios.getModel();
                model.addRow(new Object[]{p5.getUsuario(),p5.getNombre(), p5.getCorreo(), p5.getTelefono()});            
                txtnombre.setText("");
                txtusuario.setText("");
                txtcorreo.setText("");
                txttelefono.setText("");
                JOptionPane.showMessageDialog(null, "Usuario Insertado","Informacion", JOptionPane.INFORMATION_MESSAGE);                                                                            
            }
            else{
                JOptionPane.showMessageDialog(null, "Usuario ya existe","Informacion", JOptionPane.INFORMATION_MESSAGE);                                                        
            }
        }
        
    }//GEN-LAST:event_bntInsertarActionPerformed


    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:

        int row = Tablausuarios.getSelectedRow();
        if (row <0){
            JOptionPane.showMessageDialog(null, "Por favor seleccione una Fila","Informacion", JOptionPane.INFORMATION_MESSAGE);                        
        }
        else
        {
            if (ExisteUsuario()){
                int ninteger = Integer.parseInt(txttelefono.getText());
                UserEmploy p5 = new UserEmploy (txtusuario.getText(),"",txtnombre.getText(), txtcorreo.getText(), ninteger);
                try {
                    p5.ActualizarUsuario();
                } catch (SQLException ex) {
                    Logger.getLogger(PanelUsers.class.getName()).log(Level.SEVERE, null, ex);
                }
                FillTableUsers();                    
                txtnombre.setText("");
                txtusuario.setText("");
                txtcorreo.setText("");
                txttelefono.setText("");
                JOptionPane.showMessageDialog(null, "Uusario Actualizado","Informacion", JOptionPane.INFORMATION_MESSAGE);                                                            
             }
            else{
                JOptionPane.showMessageDialog(null, "No Existe el usuario","Informacion", JOptionPane.INFORMATION_MESSAGE);                                            
            }
        }
        
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        // TODO add your handling code here:
        int fila = Tablausuarios.getSelectedRow();
        if (fila <0){
            JOptionPane.showMessageDialog(null, "Por favor seleccione una Fila","Informacion", JOptionPane.INFORMATION_MESSAGE);                        
        }
        else
        {
            int a = JOptionPane.showConfirmDialog(null, "Esta seguro de borrar el usuario " +  Tablausuarios.getValueAt(fila,0)+ "?", "Select",JOptionPane.YES_NO_OPTION);                        
            if (a==0){
                String usuario = (String) Tablausuarios.getValueAt(fila,0);
                String nombre =  (String) Tablausuarios.getValueAt(fila,1);
                String correo =  (String) Tablausuarios.getValueAt(fila,2);        
                int telefono = Integer.parseInt((String) Tablausuarios.getValueAt(fila,3).toString());                
                UserEmploy p5 = new UserEmploy (usuario,"",nombre, correo, telefono);
                try {
                    p5.BorrarUsuario();
                } catch (SQLException ex) {
                    Logger.getLogger(PanelUsers.class.getName()).log(Level.SEVERE, null, ex);
                }
                FillTableUsers();                    
                txtnombre.setText("");
                txtusuario.setText("");
                txtcorreo.setText("");
                txttelefono.setText("");
                JOptionPane.showMessageDialog(null, "Uusario Borrado","Informacion", JOptionPane.INFORMATION_MESSAGE);                                                            
            }
        }
                
    }//GEN-LAST:event_btnBorrarActionPerformed

                        
